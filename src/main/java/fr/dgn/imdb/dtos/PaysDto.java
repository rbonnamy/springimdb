package fr.dgn.imdb.dtos;

public class PaysDto {
	private String nom;
	private String url;
	/** Getter
	 * @return the nom
	 */
	public String getNom() {
		return nom;
	}
	/** Setter
	 * @param nom the nom to set
	 */
	public void setNom(String nom) {
		this.nom = nom;
	}
	/** Getter
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}
	/** Setter
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}
	
}
